public class assignmentOperators {
    static void main() {
        int number_one = 2000;
        int number_two = 2;

        number_one += number_two;
        System.out.println(number_one);

        number_one -= number_two;
        System.out.println(number_one);

        number_one *= number_two;
        System.out.println(number_one);

        number_one /= number_two;
        System.out.println(number_one);

        number_one %= number_two;
        System.out.println(number_one);

        number_one++;
        System.out.println(number_one);

        number_one--;
        System.out.println(number_one);


    }
}
