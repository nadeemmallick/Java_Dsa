public class relationalOperators {
    static void main() {
        int current_price = 100;
        int sale_price = 880;

        System.out.println(current_price < sale_price);
        System.out.println(current_price == sale_price);
        System.out.println(current_price!= sale_price);
        System.out.println(current_price> sale_price);
        System.out.println(current_price>=sale_price);
        System.out.println(current_price<=sale_price);
    }
}
