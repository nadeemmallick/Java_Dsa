public class FirstLoop {
    static void main() {
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }

        for(int i = 1; i <=4; i++){
            System.out.println("Hello");
        }

        for (int i = 1; i <=10; i+=2){
            System.out.println(i);
        }
    }
}
